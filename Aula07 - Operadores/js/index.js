/* Operadores:

    1. Aritméticos
    2. Atribuição
    3. Sequência
    4. Comparação
    5. Condicional
    6. Lógicos

*/

/* 1. Aritméticos
    +  (soma)
    -  (subtração)
    *  (multiplicação)
    /  (divisão)
    ++ (incremento) -> incrementa o valor de uma variável em 1
    -- (decremento) -> decremento o valor de uma variável em 1
*/

/* 2. Atribuição
    =  (atribuição)
    += (atribuição de soma)
    -= (atribuição de subtração)
    *= (atribuição de multiplicação)
    /= (atribuição de divisão)
*/

/* 3. Sequência (Concatenação)
    + (concatenação) -> ao utilizar em duas strings, estas são concatenadas
*/

/* 4. Comparação
    ==  (igualdade) -> não considera o tipo
    === (igualdade estrita) -> considera o tipo
    !=  (diferença) -> não considera o tipo
    !== (diferença estrita) -> considera o tipo
    <   (menor que)
    >   (maior que)
    <=  (menor ou igual que)
    >=  (maior ou igual que)
*/

/* 5. Condicional
    ? : (operador ternário) -> condição ? valor se verdadeiro : valor se falso
*/

/* 6. Lógicos
    && (e)
    || (ou)
    !  (negação)
*/
