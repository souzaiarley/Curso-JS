/*
    Métodos Para Arrays
    - join: Muda o separador dos elementos no array.
    - pop: Remove o último item do array.
    - push: Adiciona um item no final do array.
    - shift: Remove o primeiro item do array.
    - unshift: Adiciona um item no início do array.
    - delete: Deleta um item do array, trocando-o por undefined.
    - splice: Insere elementos a partir de uma posição específica, podendo substituir os elementos já ou não existentes no array.
    - concat: Permite concatenar duas ou mais matrizes.
    - slice: Permite fatiar uma matriz a partir de um índice. Pode-se também adicionar um limite superior. Não altera a matriz original.
    - sort: Ordena o array (alfabeticamente).
    - reverse: Inverte o array.
    - filter: Filtra o array baseado em uma condição.
*/
